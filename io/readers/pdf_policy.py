# -*- coding: utf-8 -*-
# common_lib/io/readers/pdf_policy.py
# ============================================================
# PDF policy（正本：B案）
# - text layer 抽出後、有効文字数 < 50 なら画像PDF扱いでエラー
# - OCR は将来対応（現時点では未対応）
# ============================================================

from __future__ import annotations

from dataclasses import dataclass


# ============================================================
# policy data
# ============================================================
@dataclass(frozen=True)
class PdfPolicy:
    # ------------------------------------------------------------
    # B案：画像PDF判定のしきい値
    # ------------------------------------------------------------
    text_threshold: int = 50

    # ------------------------------------------------------------
    # 現時点では OCR 未対応
    # ------------------------------------------------------------
    ocr_supported: bool = False
