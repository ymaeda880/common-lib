# =============================================================================
# common_lib/pdf_tools/text_extract
#
# 📄 PDF Text Extract（共通ライブラリ・正本）
#
# ■ 目的
# 本パッケージは、PDFからのテキスト抽出ロジックを
# UI（Streamlitページ）から完全に分離し、
# 共通ロジックとして一元管理するための正本である。
#
# 対応機能：
#   ・テキストPDFの直接抽出
#   ・画像PDFのOCR → テキスト抽出（PyMuPDF OCR API）
#   ・PDF種別判定（text / image）
#   ・sha256による差し替え検知用ハッシュ
#   ・ダウンロードファイル名生成
#   ・抽出メタ情報モデル
#
# -----------------------------------------------------------------------------
# ■ 設計原則（重要）
#
# 1) UI非依存
#    - Streamlit（st.*）を使用しない
#    - 表示は行わない
#    - エラーは raise する（表示はページ側の責務）
#
# 2) 重い処理はページ側で制御
#    - 抽出/OCR処理自体はここで実装
#    - 実行タイミング（ボタン押下時のみなど）はページ側で制御
#
# 3) fitz import は必ずガード経由
#    - PyMuPDF（fitz）のimportは try_import_fitz() を使用
#    - import失敗時のUI表示はページ側で行う
#
# 4) PDF種別判定は軽量設計
#    - 先頭 sample_pages ページのみをスキャン
#    - 文字数が min_text_chars 以上なら text PDF
#    - それ未満なら image PDF
#    - 全ページ走査は行わない（UI応答性優先）
#
# 5) ページ番号規約
#    - UIは1-based（1〜N）
#    - 内部処理は0-based（0〜N-1）
#    - 変換はページ側で行う
#
# 6) エラー処理方針
#    - 暗号化PDFは例外をraise
#    - OCR API未対応環境は例外をraise
#    - Tesseract未導入などのOCR失敗は例外をraise
#    - 例外の表示はUI側で行う
#
# 7) Streamlit運用ルール（利用側）
#    - st.form 不使用
#    - use_container_width 不使用
#    - st.button()/st.download_button() に width 引数を使わない
#    - text_area は label_visibility="collapsed" を使用
#
# -----------------------------------------------------------------------------
# ■ ディレクトリ構成
#
# common_lib/pdf_tools/text_extract/
#     __init__.py        # re-export
#     fitz_guard.py      # PyMuPDF import安全化
#     detect.py          # text/image 判定
#     extract.py         # 直接抽出 / OCR抽出
#     utils.py           # sha256 / ファイル名生成
#     models.py          # ExtractMeta
#
# -----------------------------------------------------------------------------
# ■ 依存
#   - PyMuPDF（fitz）
#   - OCR利用時は Tesseract（環境依存）
#
# -----------------------------------------------------------------------------
# ■ 正本ポリシー
#   - PDF抽出ロジックは必ず本パッケージを修正する
#   - ページ側に抽出ロジックを書かない
#   - UI変更とロジック変更を混在させない
#
