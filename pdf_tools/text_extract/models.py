from __future__ import annotations

# ============================================================
# imports（stdlib）
# ============================================================
from dataclasses import dataclass


# ============================================================
# public model
# ============================================================
@dataclass(frozen=True)
class ExtractMeta:
    # ------------------------------------------------------------
    # 抽出方式
    # ------------------------------------------------------------
    pdf_kind: str                # "text" or "image"
    used_ocr: bool
    ocr_lang: str

    # ------------------------------------------------------------
    # 対象と処理量
    # ------------------------------------------------------------
    page_count_total: int
    pages_processed: int

    # ------------------------------------------------------------
    # timing
    # ------------------------------------------------------------
    started_at: str
    finished_at: str