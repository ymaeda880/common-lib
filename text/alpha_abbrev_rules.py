from __future__ import annotations
from typing import List, Tuple

# ルール定義：
# (key, token, replacement)
# - key        : ログ/監査用の識別子
# - token      : 正規化対象トークン（英字。大小はロジック側で吸収）
# - replacement: 置換後文字列（日本語など）
#
# 注意：
# - 長い語 → 短い語 の順で並べる（誤爆防止）
# - token は「単独トークン」として扱う前提（語境界はロジック側）

ALPHA_ABBREV_RULES: List[Tuple[str, str, str]] = [
    # --------------------------------------------------------
    # プロジェクト番号（project number）
    # --------------------------------------------------------
    ("PJNO", "pjno", "プロジェクト番号"),
    ("PJN",  "pjn",  "プロジェクト番号"),
    ("PNO",  "pno",  "プロジェクト番号"),

    # --------------------------------------------------------
    # プロジェクト（project）
    # --------------------------------------------------------
    ("PJ",   "pj",   "プロジェクト"),

    # --------------------------------------------------------
    # 年度（fiscal year）
    # --------------------------------------------------------
    ("YEAR", "year", "年度"),
    ("FY",   "fy",   "年度"),
]
