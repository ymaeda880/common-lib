# common_lib/rag_ingest/comment.py
# =============================================================================
# common_lib/rag_ingest/comment.py
#
# RAG ingest 共通基盤：設計覚書
#
# ※ 本ファイルは実行コードではない。
# ※ Markdown(.md)だと表示崩れが起きやすいため、Pythonコメントとして保存する。
# ※ 後で common_lib/rag_ingest/ 配下の実装を進めるための正本メモである。
#
# =============================================================================
# 1. この設計メモの目的
# =============================================================================
#
# このメモの目的は、RAG用ベクトルDB作成機能を
# 将来の拡張も見据えて common_lib に整理して実装するための、
# 設計方針・責務分担・データ構造・保存先・将来拡張方針を明確に残すことである。
#
# 最初に実装する対象は「報告書RAG」である。
# ただし、報告書専用の作りにしすぎると、将来以下のような別文書RAGを作る際に破綻する。
#
# - 社内規定RAG
# - 契約書RAG
# - 技術資料RAG
# - 議事録RAG
# - その他、PDFベースの文書群RAG
#
# そのため、common_lib には「報告書専用ロジック」ではなく、
# 「文書群をRAG用ベクトルDBに取り込む共通基盤」を置く。
#
# そのうえで、報告書固有の事情
# （project_master.db、year/pno、report_raw.txt/report_clean.txt、pdf_kindなど）
# は adapter 層に閉じ込める。
#
#
# =============================================================================
# 2. 全体アーキテクチャの考え方
# =============================================================================
#
# 大きく次の3層に分ける。
#
# -----------------------------------------------------------------------------
# A. UI層
# -----------------------------------------------------------------------------
#
# 例：
#   rag_builder_app/pages/160_報告書DB作成.py
#
# 役割：
# - 管理者チェック
# - 年度選択
# - 一覧取得
# - ページング
# - checkboxによる複数選択
# - 実行モード選択
# - embeddingモデル選択
# - 実行ボタン
# - 実行結果表示
#
# UI層では「見せること」と「最低限の制御」だけを担当する。
# ベクトルDB作成の本体ロジックは絶対に持たせない。
#
#
# -----------------------------------------------------------------------------
# B. 共通RAG ingest基盤
# -----------------------------------------------------------------------------
#
# 配置：
#   common_lib/rag_ingest/
#
# 役割：
# - 共通データ構造
# - path解決
# - chunk化
# - embedding呼び出し
# - meta.jsonl / processed_files.json の生成
# - vectors.npy の読み書き
# - append / 削除 / 再投入
# - 1件処理 / 複数件処理
#
# ここでは「collection が project か rules か」は意識しない。
# 代わりに、collection_id / shard_id / doc_id / attrs という抽象化で扱う。
#
#
# -----------------------------------------------------------------------------
# C. ドメインadapter層
# -----------------------------------------------------------------------------
#
# 配置例：
#   common_lib/rag_ingest/project/
#
# 役割：
# - 報告書一覧を取得する
# - 報告書1件から source 情報を組み立てる
# - image PDF / text PDF の入力元ロジックを決める
# - shard_id の決め方を定義する
# - attrs に year / pno / ocr などを入れる
#
# 将来、rules用の adapter を作る場合は別に作る。
#
#   common_lib/rag_ingest/rules/
#
#
# =============================================================================
# 3. 保存先ディレクトリ設計
# =============================================================================
#
# ベクトルDB保存先は、将来 collection が増えても整理しやすいように、
# 最初から collection 単位のフォルダを1段入れる。
#
# 保存先：
#
#   Databases/vectorstore/<collection_id>/<shard_id>/
#
# 例（報告書）：
#
#   Databases/vectorstore/project/2019/
#   Databases/vectorstore/project/2020/
#
# 例（将来の rules）：
#
#   Databases/vectorstore/rules/24a/
#   Databases/vectorstore/rules/24b/
#
# project のように year 単位で shard を切る collection もあれば、
# rules のように yy+zz など別ルールで shard を切る collection もありうる。
#
# したがって、common_lib 側では「shard_id は文字列として受け取る」設計にする。
# 「shard_id は year である」とは決め打ちしない。
#
#
# =============================================================================
# 4. シャード（shard）設計
# =============================================================================
#
# shard とは、1つの collection を物理的に分割して保存する単位である。
#
# shard を導入する理由：
#
# - ベクトルDBを巨大な1個にしないため
# - 再作成範囲を限定できるため
# - 検索対象を絞りやすくするため
# - 保守・バックアップ・差し替えがやりやすくなるため
#
# 報告書（project）では、当面 shard_id = year とする。
#
# 例：
#   collection_id = "project"
#   shard_id = "2019"
#
# 将来の rules では、year とは限らない。
# そのため shard_id は共通基盤では単なる識別文字列として扱う。
#
#
# =============================================================================
# 5. shard 配下のファイル構成
# =============================================================================
#
# shard ディレクトリの下には最低限次の3ファイルを置く。
#
# -----------------------------------------------------------------------------
# 5-1. vectors.npy
# -----------------------------------------------------------------------------
#
# embedding ベクトル本体。
#
# 想定：
# - 2次元 numpy array
# - shape = (chunk_count, embedding_dim)
#
# 重要：
# - 行順は meta.jsonl の行順と一致していなければならない
# - vectors.npy の i 行目は meta.jsonl の i 行目の chunk に対応する
#
#
# -----------------------------------------------------------------------------
# 5-2. meta.jsonl
# -----------------------------------------------------------------------------
#
# chunk 単位のメタ情報。
#
# 1行1JSON。
# vectors.npy の各行と1対1対応する。
#
# 役割：
# - 検索結果表示時に chunk本文を返す
# - source_text_path から元txtをたどる
# - source_pdf_path から元PDFをたどる
# - attrs から year/pno などの追加情報を取得する
# - 回答後に参考チャンクを表示する
# - 将来、回答後に source PDF / contract / others などを開く導線の基礎にする
#
#
# -----------------------------------------------------------------------------
# 5-3. processed_files.json
# -----------------------------------------------------------------------------
#
# 文書単位の ingest 状態を管理するファイル。
#
# 役割：
# - 未処理だけ追加する時の判定に使う
# - 同名PDF差し替え検知のために sha256 を持つ
# - どの source_text を使ったかを残す
# - どの embed_model で取り込んだかを残す
#
# 重要：
# - file名だけでは差し替え検知できないので sha256 は必須
#
#
# =============================================================================
# 6. 共通データ構造の基本概念
# =============================================================================
#
# 共通基盤では、報告書専用の year / pno を必須フィールドにしない。
#
# 代わりに次の抽象化を使う。
#
# -----------------------------------------------------------------------------
# 6-1. collection_id
# -----------------------------------------------------------------------------
#
# 何の文書集合かを示す識別子。
#
# 例：
# - "project"
# - "rules"
# - "contracts"
#
#
# -----------------------------------------------------------------------------
# 6-2. shard_id
# -----------------------------------------------------------------------------
#
# collection 内での物理分割単位。
#
# 例：
# - "2019"
# - "2020"
# - "24a"
# - "24zz"
#
#
# -----------------------------------------------------------------------------
# 6-3. doc_id
# -----------------------------------------------------------------------------
#
# 文書単位ID。
#
# 1つのPDF（または1つの取り込み単位文書）を一意に識別する。
#
# project の場合：
#   <year>/<pno>/<pdf_filename>
#
# 例：
#   2019/009/xxx.pdf
#
# doc_id は重要。
# 再投入や削除は、最終的には doc_id 単位で行う思想にする。
#
#
# -----------------------------------------------------------------------------
# 6-4. chunk_id
# -----------------------------------------------------------------------------
#
# chunk 単位ID。
#
# 形式：
#   <doc_id>#cXXXXXX
#
# 例：
#   2019/009/xxx.pdf#c000001
#
# 注意：
# - page番号ベースにしない
# - ページまたぎchunkや今後のchunk設計変更に耐えやすくする
#
#
# -----------------------------------------------------------------------------
# 6-5. attrs
# -----------------------------------------------------------------------------
#
# collection 固有の追加属性を入れる辞書。
#
# 例（project）：
#   {
#     "year": 2019,
#     "pno": "009",
#     "ocr": "no"
#   }
#
# 例（rules）：
#   {
#     "yy": "24",
#     "zz": "a",
#     "category": "employment"
#   }
#
# year/pno を共通必須フィールドにしない理由は、
# 将来 rules など別collectionで構造が変わるため。
#
#
# =============================================================================
# 7. meta.jsonl の設計詳細
# =============================================================================
#
# meta.jsonl の1行は、vectors.npy の1行に対応する chunk metadata である。
#
# 共通必須項目は以下とする。
#
# - collection_id
# - shard_id
# - doc_id
# - file
# - file_name
#
# - chunk_id
# - chunk_index
#
# - text
# - span_start
# - span_end
# - chunk_len_tokens
#
# - source_pdf_path
# - source_text_path
# - source_text_kind
#
# - sha256
# - embed_model
# - ingested_at
#
# - attrs
#
#
# -----------------------------------------------------------------------------
# 7-1. file
# -----------------------------------------------------------------------------
#
# 文書識別用の相対表現。
# project では doc_id と同じに近いが、用途として分けて持ってよい。
#
# 例：
#   2019/009/xxx.pdf
#
#
# -----------------------------------------------------------------------------
# 7-2. file_name
# -----------------------------------------------------------------------------
#
# 表示用ファイル名。
#
# 例：
#   xxx.pdf
#
# UIやログで使いやすい。
#
#
# -----------------------------------------------------------------------------
# 7-3. chunk_index
# -----------------------------------------------------------------------------
#
# 文書内でのchunk順序。
#
# 0始まりを想定。
#
#
# -----------------------------------------------------------------------------
# 7-4. text
# -----------------------------------------------------------------------------
#
# chunk本文。
#
# 回答後の「参考にしたチャンク表示」で直接使う。
# 非常に重要。
#
#
# -----------------------------------------------------------------------------
# 7-5. span_start / span_end
# -----------------------------------------------------------------------------
#
# 元の入力テキスト全体に対する文字位置。
#
# 定義：
# - span_start = chunkの開始文字位置
# - span_end   = chunkの終了文字位置（排他的終端を想定）
#
# これにより、後で元テキストに戻ったときに位置関係を確認しやすい。
#
#
# -----------------------------------------------------------------------------
# 7-6. chunk_len_tokens
# -----------------------------------------------------------------------------
#
# chunk の token 数。
#
# embedding 前後の制御や将来のchunk品質確認に使える。
#
#
# -----------------------------------------------------------------------------
# 7-7. source_pdf_path
# -----------------------------------------------------------------------------
#
# 実際の元PDFへの相対パス。
#
# 例（project）：
#   2019/009/pdf/xxx.pdf
#
# 回答後に「元PDFを表示する」導線のために必要。
#
#
# -----------------------------------------------------------------------------
# 7-8. source_text_path
# -----------------------------------------------------------------------------
#
# 実際に chunk化に使った txt への相対パス。
#
# 例：
#   2019/009/text/report_clean.txt
#   2019/009/text/report_raw.txt
#
# 回答後に「参考にしたchunkの元テキストを確認する」ために必要。
#
# 重要：
# - source_text_path は必要
# - ただしこれだけでは不十分
# - source_pdf_path も別で持つ
#
#
# -----------------------------------------------------------------------------
# 7-9. source_text_kind
# -----------------------------------------------------------------------------
#
# 何種類のテキストを使ったかを明示する。
#
# 例：
# - "clean"
# - "raw"
#
# project の text PDF では raw を使うことが多いが、
# 将来的に clean を使うケースにも対応できるようにしておく。
#
#
# -----------------------------------------------------------------------------
# 7-10. sha256
# -----------------------------------------------------------------------------
#
# 元文書（基本的にはPDF）のハッシュ。
#
# 目的：
# - 同名差し替え検知
# - 再投入要否判定
# - どの版の文書から作られたchunkかを記録
#
#
# -----------------------------------------------------------------------------
# 7-11. embed_model
# -----------------------------------------------------------------------------
#
# 使用した embedding model 名。
#
# 例：
#   text-embedding-3-large
#
# 将来モデル変更した際の混在検知に使える。
#
#
# -----------------------------------------------------------------------------
# 7-12. ingested_at
# -----------------------------------------------------------------------------
#
# 取り込み日時（UTCのISO 8601文字列を想定）。
#
# 例：
#   2025-10-04T00:30:50Z
#
#
# =============================================================================
# 8. processed_files.json の設計詳細
# =============================================================================
#
# processed_files.json は chunk 単位ではなく、文書単位の記録。
#
# 共通項目：
#
# - collection_id
# - shard_id
# - doc_id
# - file
#
# - sha256
#
# - source_pdf_path
# - source_text_path
# - source_text_kind
#
# - embed_model
# - ingested_at
#
# - attrs
#
# 用途：
# - 未処理だけ追加
# - 同名差し替え検知
# - 再投入要否の確認
#
# 将来、必要なら以下も追加候補：
# - chunk_count
# - vector_rows
# - status
#
#
# =============================================================================
# 9. 報告書（project）固有の source 解決ロジック
# =============================================================================
#
# project 用 adapter では、報告書特有の事情を扱う。
#
# 報告書1件について、最終的に共通 ingest に渡す source 情報を解決する。
#
# project 固有で考慮するもの：
#
# - project_year
# - project_no(pno)
# - pdf_filename
# - pdf_kind
# - pdf_lock_flag
# - OCR済みか
# - report_raw.txt の有無
# - report_clean.txt の有無
# - sha256
#
#
# -----------------------------------------------------------------------------
# 9-1. 入力元テキストの基本方針
# -----------------------------------------------------------------------------
#
# 画像PDF（image pdf）の場合：
#   report_clean.txt を使う
#
# テキストPDF（text pdf）の場合：
#   基本は report_raw.txt
#   ただし clean が存在するケースも将来ありうるため、
#   コードでは「clean があれば使える」「少なくとも clean が無ければ raw」
#   という柔軟なフォールバック設計にする
#
# ただし実装時の実際の優先順位は adapter 側で明確に固定すること。
#
#
# -----------------------------------------------------------------------------
# 9-2. project の attrs
# -----------------------------------------------------------------------------
#
# attrs には最低限以下を入れる。
#
#   {
#     "year": <int>,
#     "pno": <str>,
#     "ocr": "yes" or "no"
#   }
#
#
# -----------------------------------------------------------------------------
# 9-3. project の collection_id / shard_id
# -----------------------------------------------------------------------------
#
# collection_id = "project"
#
# shard_id = str(year)
#
#
# =============================================================================
# 10. project_master.db との関係
# =============================================================================
#
# project_master.db の path を meta.jsonl に保存する必要は基本的にない。
#
# 理由：
# - project名や発注先などは「回答時点の最新DB」を見たい
# - 取り込み時点の path を残しても、後で古くなるだけ
# - DB path は環境依存になりやすい
#
# したがって、meta 側には DB path を持たせず、
# 回答時には attrs の year / pno を使って live に project_master.db を引く。
#
# つまり、
# - 静的出典情報は meta が持つ
# - 動的業務情報は回答時にDBから取得する
#
# という分離にする。
#
#
# =============================================================================
# 11. 回答後の参照導線を支える情報
# =============================================================================
#
# bot の回答後には、次のような表示をしたい。
#
# - 参考にしたチャンク本文を表示
# - 必要なら元PDFを表示
# - 必要なら contract フォルダ配下のファイルを表示
# - 必要なら others フォルダ配下のファイルを表示
# - project_master.db からプロジェクト名等を live 取得して表示
#
# このとき meta に必要なのは次の情報である。
#
# - source_text_path
# - source_pdf_path
# - attrs.year
# - attrs.pno
#
# contract フォルダや others フォルダの「個別パス」を meta に全て持つ必要はない。
# year / pno があれば
#
#   Archive/project/<year>/<pno>/
#
# を起点にたどれるためである。
#
# つまり、meta は
# 「このchunkがどの案件の、どのPDFの、どのtxtから作られたか」
# までを持てばよい。
#
#
# =============================================================================
# 12. chunk設計の方針
# =============================================================================
#
# chunk化は common_lib/rag_ingest/chunk_ops.py に切り出す。
#
# 基本方針：
# - 日本語文書向け
# - 文の途中で極力切らない
# - ページ境界依存を弱くする
# - page番号ベースのchunk_idにしない
#
# 将来的に page 対応を厳密化したければ、
# start_page / end_page などを別途拡張できるようにする。
#
# ただし初期実装では、まず
# - text
# - span_start
# - span_end
# - chunk_index
# - chunk_len_tokens
# を安定して持つことを優先する。
#
#
# =============================================================================
# 13. embedding 実行方針
# =============================================================================
#
# embedding 実行は、必ず
#
#   toolkit_app/pages/108_ベクトル埋込（テンプレ）.py
#
# の思想に準拠する。
#
# 重要ルール：
#
# - provider 直叩き禁止
# - common_lib.ai.embed_text を使う
# - busy_run を必ず使う
# - task_type = "embedding"
# - tokens / cost は推計しない
# - 返ってきた usage / cost だけ使う
#
# common_lib/rag_ingest/embedding_ops.py は、
# ページテンプレの思想をページ依存なしで共通化した正本にする。
#
#
# =============================================================================
# 14. vectorstore I/O の方針
# =============================================================================
#
# common_lib/rag_ingest/vectorstore_io.py では、
# shard ディレクトリ配下の3ファイルのI/Oを担当する。
#
# 責務：
# - shard path 解決
# - shard directory 作成
# - vectors.npy 読込 / 保存
# - meta.jsonl 読込 / 保存
# - processed_files.json 読込 / 保存
# - append
# - doc単位削除
# - 再保存
# - 件数整合確認
#
# 重要な整合条件：
# - vectors.npy の行数 == meta.jsonl の行数
#
# この整合が崩れると retrieval が壊れる。
# 必ず正本I/O側で検査する。
#
#
# =============================================================================
# 15. 実行モード
# =============================================================================
#
# 160_報告書DB作成.py では、少なくとも次の2モードを持つ。
#
# -----------------------------------------------------------------------------
# 15-1. 未処理だけ追加
# -----------------------------------------------------------------------------
#
# processed_files.json を見て、未処理文書だけ ingest する。
#
# 用途：
# - 新規PDF追加後の通常運用
#
#
# -----------------------------------------------------------------------------
# 15-2. 再投入
# -----------------------------------------------------------------------------
#
# 選択文書の既存chunkを削除し、再度 ingest する。
#
# 用途：
# - PDF差し替え
# - OCRやcleanの再実行後
# - chunk設計変更後
# - embedding model 変更後
#
# 共通基盤では doc_id 単位再投入の思想で実装する。
# project の場合は、実質的には year/pno/pdf_filename に対応する。
#
#
# =============================================================================
# 16. common_lib/rag_ingest/ のファイル構成（予定）
# =============================================================================
#
# common_lib/rag_ingest/
#
#   models.py
#     - 共通データ構造
#
#   paths.py
#     - Databases/vectorstore/<collection_id>/<shard_id>/ 解決
#
#   chunk_ops.py
#     - text → chunks
#
#   embedding_ops.py
#     - embed_text / busy_run / token/cost反映
#
#   manifest_ops.py
#     - meta / processed レコード生成
#     - doc_id / chunk_id 規約
#
#   vectorstore_io.py
#     - vectors/meta/processed I/O
#
#   ingest_usecase.py
#     - 1文書取り込み
#
#   rebuild_usecase.py
#     - 再投入
#
#   batch_usecase.py
#     - 複数件実行
#
#
# =============================================================================
# 17. common_lib/rag_ingest/project/ のファイル構成（予定）
# =============================================================================
#
# common_lib/rag_ingest/project/
#
#   source_adapter.py
#     - 報告書1件 → 共通 ingest source に変換
#
#   status_ops.py
#     - 160ページ一覧表示用の状態判定
#
#   selectors.py
#     - 年度一覧、報告書一覧の取得補助
#
#
# =============================================================================
# 18. 160ページ本体の責務（再確認）
# =============================================================================
#
# rag_builder_app/pages/160_報告書DB作成.py
#
# UIは次に準拠する。
#
# -----------------------------------------------------------------------------
# 18-1. 年度選択〜一覧選択
# -----------------------------------------------------------------------------
